const generateCandles = (basePrice, count = 50) => {
  const candles = [];
  let price = basePrice * 0.95;
  const now = Date.now();
  for (let i = count; i >= 0; i--) {
    const open = price;
    const close = parseFloat(
      (price * (1 + (Math.random() - 0.48) * 0.02)).toFixed(6),
    );
    const high = parseFloat(
      (Math.max(open, close) * (1 + Math.random() * 0.005)).toFixed(6),
    );
    const low = parseFloat(
      (Math.min(open, close) * (1 - Math.random() * 0.005)).toFixed(6),
    );
    candles.push({
      time: now - i * 3600000,
      open,
      high,
      low,
      close,
      volume: parseFloat((Math.random() * 1000000).toFixed(2)),
    });
    price = close;
  }
  return candles;
};

export const getMarkets = async (req, res) => {
  try {
    const { assetClass, category } = req.query;
    let entries = Object.entries(MARKET_DATA).map(([symbol, data]) => ({
      symbol,
      ...data,
    }));
    if (assetClass)
      entries = entries.filter((m) => m.assetClass === assetClass);
    if (category) entries = entries.filter((m) => m.category === category);
    return successResponse(res, 200, "Markets fetched", {
      markets: entries,
      total: entries.length,
    });
  } catch (err) {
    return errorResponse(res, 500, err.message);
  }
};

export const getMarket = async (req, res) => {
  try {
    const symbol = req.params.symbol.toUpperCase().replace("-", "/");
    const data = MARKET_DATA[symbol];
    if (!data) return errorResponse(res, 404, `Market ${symbol} not found`);
    return successResponse(res, 200, "Market fetched", {
      symbol,
      ...data,
      candles: generateCandles(data.price),
    });
  } catch (err) {
    return errorResponse(res, 500, err.message);
  }
};

export const getTicker = async (req, res) => {
  try {
    const tickers = Object.entries(MARKET_DATA).map(([symbol, d]) => ({
      symbol,
      price: d.price,
      change24h: d.change24h,
      up: d.change24h >= 0,
      assetClass: d.assetClass,
      category: d.category,
    }));
    return successResponse(res, 200, "Ticker data", { tickers });
  } catch (err) {
    return errorResponse(res, 500, err.message);
  }
};

